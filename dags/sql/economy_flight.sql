CREATE TABLE IF NOT EXISTS economic_flight (
    date VARCHAR(50),
    airline VARCHAR(50),
    ch_code VARCHAR(50),
    num_code INTEGER,
    dep_time VARCHAR(50),
    "from" VARCHAR(50),
    time_taken VARCHAR(50),
    stop VARCHAR(50),
    arr_time VARCHAR(50),
    "to" VARCHAR(50),
    price INTEGER,
    class VARCHAR(50)
);
